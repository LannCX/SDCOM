class CosineDistance(nn.Module):
    def __init__(self):
        super(CosineDistance, self).__init__()

    def forward(self, a, b):
        # a: BxL
        # b: BxL
        length = torch.sqrt(torch.sum(a*a, dim=-1))*torch.sqrt(torch.sum(b*b, dim=-1))
        prod = torch.sum(a*b, dim=-1)
        # Generally, we use 1-cosine(\theta) to represent the positive relationship between the angle and similarity. 
        # Here we want to enlarge the distance, which need a negative relationship, so we use 1+cosine(\theta)
        return 1+torch.mean(prod/length)


